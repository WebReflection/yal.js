
    function preload(queue) {
        for (i = 0; i < queue[length]; ++i) {
            if (!queue[i]) continue;    //:lint it's meant to be like this
            preloading[push](parentNode = documentElement[insertBefore](
                window.opera || !+"\v1" ?
                    new Image :
                    document[createElement]("object")
                ,
                documentElement[lastChild]
            ));
            parentNode.width = parentNode.height = 0;   //:lint it's meant to be like this
            parentNode.src = parentNode.data = parentNode._ = queue[i]; //:lint it's meant to be like this
        }
        return queue;
    }
    
    